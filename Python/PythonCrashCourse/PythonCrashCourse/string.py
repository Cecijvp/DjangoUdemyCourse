value2 = "The last meal"

result1 = value1 + value2 #Concatenar
print(result1)

result2 = value1 * 2 #repetir
print(result2)

result3 = value1[5]  #regresa la letra en esa posición
print(result3)

result4 = value2[1:5] #regresa todas las letras que hay en esas posiciones
